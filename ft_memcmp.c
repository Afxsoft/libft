#include "libft.h"

int		ft_memcmp(void const *s1, void const *s2, size_t n)
{
	unsigned char	*tmp1;
	unsigned char	*tmp2;
	size_t			i;

	tmp1 = (unsigned char *)s1;
	tmp2 = (unsigned char *)s2;
	i = 0;
	while (tmp1[i] == tmp2[i] && i < n)
		i++;
	if (i == n)
		return (0);
	return (tmp1[i] - tmp2[i]);
}
