#include "libft.h"

char    *ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*w;

	i = 0;
	w = ft_strnew(ft_strlen(s));
	while (s[i])
	{
		w[i] = (*f)(s[i]);
		i++;
	}
	return (w);
}
